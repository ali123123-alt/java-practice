public class Question1 {
    public static void main(String args[]){
        int a=10;
        int b=20;
        //relational operators
        System.out.println(a==b);
        System.out.println(a<b);
        //bitwise operators
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(a^b);
        //logical operators
        System.out.println((5>3)&&(8>5));
        System.out.println((5>3)||(8>5));
    }
}
